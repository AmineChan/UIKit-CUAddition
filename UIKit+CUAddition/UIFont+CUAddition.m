#import "UIFont+CUAddition.h"

inline UIFont* CUSystemFont(CGFloat size)
{
    return [UIFont systemFontOfSize:size];
}

inline UIFont* CUBoldSystemFont(CGFloat size)
{
    return [UIFont boldSystemFontOfSize:size];
}

@implementation UIFont (CUAddition)

@end
